    private void altilloActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_altilloActionPerformed
        try{
            String nom=nom_esp.getText();
            String Lug=numLug.getText();
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/reservacion","root","");
            String sql="Insert into espacios (Nombre_espacio, Numero_lugares) Values ('"+nom+"','"+Lug+"')";
            Statement res= con.createStatement();
            res.executeUpdate(sql);
            JOptionPane.showMessageDialog(null,"Registro completo");
            con.close();
            nom_esp.setText(null);
            numLug.setText(null);
            }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
        
    }//GEN-LAST:event_altilloActionPerformed

