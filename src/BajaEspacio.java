    private void bjsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bjsActionPerformed
        try{
        Class.forName("com.mysql.jdbc.Driver");
        Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/reservacion","root","");
        int seleccion = JOptionPane.showOptionDialog(null,"Deseas borrar el Espacio?","Confirma el borrado",
        JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE,null,
        new Object[] { "Borrar", "Cancelar"}, "Borrar");
        if(seleccion==0){
            String borra = (String)Espa.getSelectedItem();
            Statement rs = con.createStatement();
            rs.executeUpdate("DELETE FROM espacios WHERE Nombre_espacio='"+borra+"'");
            JOptionPane.showMessageDialog(null,"Registro eliminado");
            con.close();
            MenuAdm menu1=new MenuAdm();
            menu1.setVisible(true);
            dispose();
            
    }
        else{
            MenuAdm menu1=new MenuAdm();
            menu1.setVisible(true);
            dispose();
        }
    }                                         
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_bjsActionPerformed

