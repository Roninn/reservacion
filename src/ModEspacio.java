    private void consulActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_consulActionPerformed
        String espa=(String)Espa.getSelectedItem();
        String IDD=null;
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/reservacion","root","");
            String q="SELECT * FROM espacios WHERE Nombre_espacio='"+espa+"'";
            PreparedStatement pts=con.prepareStatement(q);
            ResultSet fs=pts.executeQuery();
            while(fs.next()){
                 IDD=fs.getString(1);
            }
            String p= "SELECT * FROM espacios WHERE id_espacio='"+IDD+"'";
            PreparedStatement pst=con.prepareStatement(p);
            ResultSet rs=pst.executeQuery();
            while(rs.next()){
                Name.setText(rs.getString(2));
                NumLug.setText(rs.getString(3));
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_consulActionPerformed

